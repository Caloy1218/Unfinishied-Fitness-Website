function darkMode() {
  document.querySelector(".Tdee-Container").style.background = "#1b1c1e";
  document.querySelector(".Tdee-Container").style.color = "white";
}
function lightMode() {
  document.querySelector(".Tdee-Container").style.background = "white";
  document.querySelector(".Tdee-Container").style.color = "#1b1c1e";
}
