function darkMode() {
  document.querySelector(".upper-header").style.background = "#1b1c1e";
  document.querySelector(".about-container").style.background = "#1b1c1e";
  document.querySelector(".about-semi-container").style.background = "#1b1c1e";
  document.querySelector(".about-semi-container").style.color = "white";
  document.querySelector(".motivational-quote").style.color = "white";
}
function lightMode() {
  document.querySelector(".about-container").style.background = "white";
  document.querySelector(".about-semi-container").style.background = "white";
  document.querySelector(".about-semi-container").style.color = "#1b1c1e";
  document.querySelector(".motivational-quote").style.color = "#1b1c1e";
}
