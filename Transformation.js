function darkMode() {
  document.querySelector(".transformation-container").style.background =
    "#1b1c1e";
  document.querySelector(".text-content").style.color = "white";
  document.querySelector(".text-content-bottom").style.color = "white";
}
function lightMode() {
  document.querySelector(".transformation-container").style.background =
    "white";    
  document.querySelector(".text-content").style.color = "#1b1c1e";
  document.querySelector(".text-content-bottom").style.color = "#1b1c1e";
}
